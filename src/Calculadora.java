
public class Calculadora 
{
	public int sumar(int a,int b)
	{
		int resultado = a + b;
		return resultado;
	}
	
	public int restar(int a,int b)
	{
		int resultado = a - b;
		return resultado;
	}
	
	public float dividir(int a,int b)
	{
		float resultado = a / b;
		return resultado;
	}
	
	public double log10(int a)
	{
		double resultado = java.lang.Math.log10(a);
		return resultado;
	}
	
	
	
}
